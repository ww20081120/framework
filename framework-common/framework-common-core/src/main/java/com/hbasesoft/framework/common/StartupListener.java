package com.hbasesoft.framework.common;

import org.springframework.context.ApplicationContext;

import com.hbasesoft.framework.common.utils.PropertyHolder;

/**
 * <Description> <br>
 * 
 * @author 王伟<br>
 * @version 1.0<br>
 * @taskId <br>
 * @CreateDate 2015年12月7日 <br>
 * @since V1.0<br>
 * @see com.hbasesoft.framework.web.core.init <br>
 */
public interface StartupListener {

    /**
     * Description: <br>
     * 
     * @author 王伟<br>
     * @taskId <br>
     * @return <br>
     */
    default boolean enable() {
        return PropertyHolder.getBooleanProperty(this.getClass().getName(), true);
    }

    /**
     * Description: <br>
     * 
     * @author 王伟<br>
     * @taskId <br>
     * @return <br>
     */
    default LoadOrder getOrder() {
        return LoadOrder.LAST;
    }

    /**
     * Description: <br>
     * 
     * @author 王伟<br>
     * @taskId <br>
     *         <br>
     */
    default void init() {
    }

    /**
     * Description: <br>
     * 
     * @author 王伟<br>
     * @taskId <br>
     * @param context <br>
     */
    default void complete(ApplicationContext context) {
    }

    /**
     * Description: <br>
     * 
     * @author 王伟<br>
     * @taskId <br>
     *         <br>
     */
    default void destory() {
    }

    enum LoadOrder {
        /** order */
        FIRST, MIDDLE, LAST
    }

}
